package com.example.austdiary;

public class AboutItem {

	private String AboutName;
	private int iconId;
	public AboutItem(String aboutName, int iconId) {
		super();
		this.AboutName = aboutName;
		this.iconId = iconId;
	}
	public String getAboutName() {
		return AboutName;
	}
	public int getIconId() {
		return iconId;
	}
	
	
}
